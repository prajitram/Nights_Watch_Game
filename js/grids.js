// Prints a passed in grid to the console to easily copy-paste to grids.js.
// Used for changing grids permanently after messing around in grid editor.
window.printGrid = function(g){
	var r = "window.grid = [\n";
	for (var i = 0; i < g.length; i++){
		r += "\t\t\t   [";
		for (var j = 0; j < g[i].length; j++){
			r += g[i][j];
			if (j != g[i].length-1)
				r += ", ";
		}
		r += "]";
		if (i != g.length-1)
			r += ",";
		r += '\n';
	}
	r += "\t\t     ];"
	console.log(r);
}

